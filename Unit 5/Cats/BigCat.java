public class BigCat
{
    public void act()
    {
        wake();
        yawn();
        roar();
        eat();
        sleep();
    }

    public void wake() { System.out.println("Wake like a cat.");}
    public void yawn() { System.out.println("Yawn like a cat.");}
    public void roar() { System.out.println("Roar like a cat.");}
    public void eat() { System.out.println("Eat like a cat.");}
    public void sleep() { System.out.println("Sleep like a cat.");}
}